      <p>Le Centre d’Orientation et de Planification de l’Education (COPE) de Rabat Hay Riyad est une institution marocaine qui a pour mission de coordonner et de planifier les activités éducatives dans la région de Rabat-Salé-Kénitra.</p>
       <H5>Les principales fonctions du COPE de Rabat Hay Riyad sont les suivantes :</H5>
       <p>Planification de l’éducation : Le COPE élabore des plans et des stratégies pour améliorer la qualité de l’éducation dans la région, en se basant sur les besoins et les objectifs éducatifs du pays.</p>
       <p><h5>Orientation des élèves :</h5>  Le COPE fournit des informations et des conseils aux élèves pour les aider à choisir les filières d’études les plus adaptées à leurs aspirations et à leurs capacités.</p>
       <p><h5>Formation des enseignants :</h5>  Le COPE organise des formations pour les enseignants et les cadres éducatifs, afin de renforcer leurs compétences et leur permettre d’appliquer les nouvelles méthodes et technologies pédagogiques.</p>
       <p><h5>Coordination des projets éducatifs :</h5>  Le COPE coordonne les projets éducatifs entre les différentes institutions et les partenaires sociaux, pour assurer une meilleure efficacité et une utilisation optimale des ressources disponibles.</p>
 
